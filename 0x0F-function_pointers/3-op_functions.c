#include "3-calc.h"

/**
 * op_add - returns sum of two numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference of two numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - return the product of two numbers
 * @a: int 1
 * @b: int 2
 *
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - returns the division of a by b
 * @a: int 1
 * @b: int 2
 *
 * Return: division
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - returns the remainder of division of a by b
 * @a: int 1
 * @b: int 2
 *
 * Return: division
 */

int op_mod(int a, int b)
{
	return (a % b);
}
